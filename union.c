#include <unistd.h>

int main (int ac, char **av)
{
	int hash_t[256];
	int i;

	if (ac == 3)
	{
		for (i = 0; i < 256; ++i)
			hash_t[i] = 0;
		while (*av[1])
		{
			if (!hash_t[*av[1]])
			{
				hash_t[*av[1]] = 1;
				write(1, av[1], 1);
			}
			++av[1];
		}
		while (*av[2])
		{
			if (!hash_t[*av[2]])
			{
				hash_t[*av[2]] = 1;
				write(1, av[2], 1);
			}
			++av[2];
		}
	}
	write(1, "\n", 1);
	return (0);
}
